
public class CalculatorOperations {
	public int add(int num1,int num2) {
		return num1+num2;
	}
	public float add(float num1,float num2) {
		return num1+num2;
	}
	public int subtract(int num1,int num2) {
		return num1-num2;
	}
	public float subtract(float num1,float num2) {
		return num1-num2;
	}
	
	
}
